#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*new;
	int		i;

	new = malloc(sizeof(src));
	i = 0;
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
