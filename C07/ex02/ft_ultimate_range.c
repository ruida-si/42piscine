#include <stdlib.h>

int	ft_ultimate_range(int **range, int min, int max)
{
	int	i;

	if (min >= max)
	{
		*range = NULL;
		return (0);
	}
	*range = malloc(sizeof(int) * (max - min));
	if (*range == NULL)
	{
		*range = NULL;
		return (-1);
	}
	i = 0;
	while (max > min)
		(*range)[i++] = min++;
	return (i);
}
