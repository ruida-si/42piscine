#include <unistd.h>

void	ft_print_comb2(void)

{
	int	a;
	int	b;

	a = 0;
	while (a < 99)
	{
		b = a + 1;
		while (b < 100)
		{
			write(1, &((char []){a / 10 + '0'}), 1);
			write(1, &((char []){a % 10 + '0'}), 1);
			write(1, " ", 1);
			write(1, &((char []){b / 10 + '0'}), 1);
			write(1, &((char []){b % 10 + '0'}), 1);
			if (a != 98)
				write(1, ", ", 2);
			b++;
		}
		a++;
	}
}
